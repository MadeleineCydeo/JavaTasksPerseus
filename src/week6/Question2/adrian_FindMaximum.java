package week6.Question2;

public class adrian_FindMaximum {
    public static void main(String[] args) {


        int maxNum = 101;
        for (int i = 0; i > 5; i++) {
            if (i <= maxNum) {
                maxNum = i;
            }
        }
        System.out.println("maximum number is: " + maxNum);

    }
}